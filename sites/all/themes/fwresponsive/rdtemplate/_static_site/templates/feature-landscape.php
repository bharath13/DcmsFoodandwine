          <p>It’s a market off the tourist circuit—stalls sell clothes and shiny pink backpacks for kids, plastic colanders and socks, piñatas like spiky Bethlehem stars at every stage of construction. In the illusory green light of fluorescent tubes, Olvera ambles down one of the lanes toward the skylit center, and the still-quiet, not-yet-open comida corrida booths, home to Mexico City’s cheap midday meals. He points out sapotes negros—rich, chocolate-colored cousins of the persimmon—and a cardboard box of black-spotted bananas, each no bigger than a baby’s foot. “Fruit in Mexico is always overripe,” he says, holding up a hand of decaying bananas, signaling to the girl that he wants to buy. “It’s the taste of moles,</p>
