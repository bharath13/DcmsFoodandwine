          <p>Olvera looks slightly withered, though. This week while he’s in Mexico, his life is a tight grid of two-hour calendar blocks. He just wrapped his epic English-language book for Phaidon, Mexico from the Inside Out, which traces the life of Pujol. Now he has to approve the type of padded envelopes that review copies will be mailed in, go over blueprints for Manta—a restaurant at The Cape hotel in Mexico’s Los Cabos—and taste the tiny chia-seed tostada one of his sous-chefs has tracked him down to try. He also wants to take me to his laid-back café, Eno, just down the street, where the menu is stocked with breakfast and lunch standards like eggs with chorizo and quinoa salad.</p>
          <p>But this morning, Olvera is most interested in taking me around Mexico City to try the classic dishes he loves best. He says, “A lot of people in Mexico are like, ‘Oh, now that you’re in New York you don’t give a shit about us anymore.’ That’s wrong: Pujol is the first, the most important restaurant.” At Pujol, Olvera’s inspiration drips through a filter of thought and technique, but in New York City he traces a more direct line to the foods he loves, some since he was a kid. Cosme is where Olvera is freer to be himself, truer to his food memories. New York, in a weird way, brings Olvera closest to Mexico City. On a Monday morning, just after 10, not a lot is happening at Mercado San Cosme. Olvera’s grandfather lived six blocks away, in Colonia San Rafael, and little-kid Enrique hung out here.</p>
