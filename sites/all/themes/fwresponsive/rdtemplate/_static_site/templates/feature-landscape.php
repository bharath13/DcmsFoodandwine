          <p>Dinner at Pujol can invade your senses like a distillation of Mexico. You feel its sweep in a series of small, expressive gestures: the first spring escamoles (ant larvae), bursting from a single pea pod, a statement of birth. Or the contrasting moles: a two-year-old mole madre, brown as earth, perpetually fed with new mole; and a brick-red mole nuevo. Both are spread on the plate as a disk within a circle, like some reductive Aztec calendar. Olvera is a chef who thinks a lot about time. When Cosme opened, critics pored over the dishes to divine some authentic expression of Mexico. Some found it. Others focused on Olvera’s genius as a restaurateur, his charisma, the way he studies his clientele and gives them what they want in a place thousands of miles from his source of inspiration. Right now, kitty-corner from me at the massive table in the private dining room at Pujol, just off the burnished galley of a prep kitchen that’s animated by a dozen cooks, Olvera is trying to find his voice in the literal sense. After two months away, he got back to Mexico City with a raw throat ravaged by a cold. He’s sipping an infusion of ginger and lime, poured from a murky French press one of his cooks has just delivered.</p>
