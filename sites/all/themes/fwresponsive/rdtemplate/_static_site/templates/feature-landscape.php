          <p>Ghosts are everywhere in Mexico City, looming massively over the living. At the highest point in Bosque de Chapultepec, the city’s gracefully scrubby central park, the dead are stacked up like churros in David Alfaro Siqueiros’s 1957 mural of martyrs to the Revolution. Closer to earth, in the posh apartment zone of Polanco, chef Enrique Olvera’s dead grandfather—photographed like a silver-screen star—gazes like a specter on everyone entering or leaving the city’s most famous restaurant, Pujol.</p>
          <p>I’m in Mexico City for a ghost tour of my own: I want to find the dishes that haunt Olvera’s memory. This is the food that inspires the 39-year-old hero of modern Mexican gastronomy, both at Pujol and at his year-old New York City restaurant, Cosme. Olvera, more than any other chef, convinced the world that Mexico City is an essential pin on every global map of food. He grew up in and around here, heading to the US to study at the Culinary Institute of America in Hyde Park, New York. In 2000, he opened Pujol—a slurring of pozole, his nickname in school, a burn for being pudgy. His food was heavily influenced by The French Laundry Cookbook: New American tweaked with Mexican ingredients, as in foie gras with guava. It wasn’t until 2005 that Olvera found his own</p>
